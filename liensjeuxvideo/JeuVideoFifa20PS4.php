						- Modes : <br />

						- Coup d’envoi VOLTA : emmenez vos équipes professionnelles dans la rue et évoluez dans les nombreux lieux VOLTA et avec différents formats de matchs. Disputez un PSG - OL à Paris, le derby madrilène dans des rues espagnoles ou l’affiche Chelsea - Tottenham dans une cage à Londres. <br />

						- Univers VOLTA : améliorez votre équipe VOLTA en disputant des matchs joueur solo contre des formations générées par la communauté sous différents formats et en différents endroits. Après chaque victoire, recrutez l’un des joueurs adverses et créez votre équipe en privilégiant la technique et le collectif. <br />

