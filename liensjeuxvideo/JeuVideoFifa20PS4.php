						JOUABILITÉ <br />

						Découvrez le FOOTBALL INTELLIGENT, une refonte complète qui vous offre un réalisme sans précédent. Grâce au FOOTBALL INTELLIGENT, chaque moment sur le terrain prend une autre envergure : avec le ballon, sans le ballon et dans les espaces. Trois piliers : Moments Décisifs (avec le ballon), Fluidité Réaliste (sans le ballon) et Physique du Ballon (dans les espaces). Cette approche vise à placer l’utilisateur au centre de chaque match. <br />

						- Fluidité Réaliste : Les qualités fondamentales des joueurs de classe mondiale passent par une prise de conscience du temps, des espaces et du placement. Grâce à la Fluidité réaliste, chaque joueur contrôlé par l’IA se comportera de façon à prendre en compte ses adversaires afin de pouvoir se dégager du temps et de l’espace et de placer le joueur au centre de l’action. <br />

						- Un-contre-un Dynamiques : créé à partir du tempo des plus grands championnats, le rythme de jeu totalement repensé offre des espaces et une vitesse plus réalistes, et il en résulte des un-contre-un où l’accent est mis sur les Moments décisifs contrôlés par le joueur.
						- Défense de l’IA : le système de placement et de tacle a été repensé afin de créer un environnement où l’IA est plus complémentaire avec le joueur contrôlé par l’utilisateur. L’IA apporte un soutien défensif plus adapté et l’art de la défense devient plus avantageux et gratifiant pour l’utilisateur.
						- Mouvements Naturels : grâce à des innovations dans les déplacements et le placement, les joueurs de l’IA évoluent de façon plus naturelle et plus calme qu’à l’accoutumée, créant ainsi des mouvements plus réalistes pour tous les joueurs sur le terrain. <br />

						- Moments Décisifs : Dans le football, l’issue des matchs dépend souvent d’une série de moments clés. De la Finition Sang-Froid dans les trente derniers mètres aux Tacles Maîtrisés en défense, les matchs se gagnent ou se perdent sur les Moments Décisifs contrôlés par l’utilisateur. <br />

						- Dribbles Latéraux : la nouvelle mécanique de Dribbles Latéraux donne encore plus de profondeur aux options offensives. Il est désormais possible de se déplacer avec plus de souplesse qu’avant pour prendre l’adversaire de vitesse ou le déstabiliser sur un geste technique. <br />
						- Tacles Maîtrisés : grâce au Système de Contrôle Actif, la défense déclenchée par le joueur devient plus avantageuse et gratifiante grâce à de nouvelles animations de tacles. Plus le tacle est propre, plus la probabilité de récupérer le ballon est élevée. <br />
						- Refonte des Coups de Pied Arrêtés : ce nouveau système offre plus d’options de visée sur les coups francs directs et les pénaltys, notamment avec la création d’une mécanique de tirs basée sur la technique qui devient ainsi plus gratifiante. <br />
						- Finition Sang-Froid : le mécanisme des tirs a été retravaillé pour vous offrir plus de régularité et de précision dans la finition. Les frappes de balle deviennent plus réalistes et plus gratifiantes et le bénéfice risque/récompense est plus marqué que jamais dans les situations les plus délicates comme les reprises de volée. <br />

						- Physique du Ballon : le principal composant dynamique d’un match de football est le ballon. La Physique du Ballon permet d’obtenir un degré de réalisme encore jamais atteint avec de nouvelles trajectoires de tirs, des interactions plus réalistes sur les tacles et un comportement du ballon basé sur les lois de la physique. <br />

						- Déplacement Cohérent : le nouveau système de déplacement permet d’obtenir des effets et des rebonds du ballon encore plus réalistes. L’expérience de jeu paraîtra ainsi plus réelle que jamais. <br />
						- Nouvelles Trajectoires de Tirs : le nouveau système de mouvement du ballon permet d’obtenir des trajectoires encore plus réalistes sur les frappes. Vous pourrez ainsi déclencher des frappes travaillées, flottantes, enroulées ou sans contrôle et à la trajectoire montante. <br />

